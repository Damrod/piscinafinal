char	*ft_strcat(char *dest, char *src)
{
	char *d;

	d = dest;
	while (*d != '\0')
		d++;
	while (*src != '\0')
	{
		*d = *(unsigned char*)src;
		src++;
		d++;
	}
	*d = '\0';
	return (dest);
}
