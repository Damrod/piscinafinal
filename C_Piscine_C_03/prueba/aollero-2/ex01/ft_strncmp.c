int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int					a;
	unsigned int		i;

	i = 0;
	a = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0') && (i < n - 1))
		i++;
	a = s1[i] - s2[i];
	if (n == 0)
		a = 0;
	return (a);
}
