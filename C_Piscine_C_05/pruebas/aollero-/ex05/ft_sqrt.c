int		ft_sqrt(int nb)
{
	int aux;

	aux = 1;
	while (aux <= 46340)
	{
		if (aux * aux == nb)
			return (aux);
		if (aux * aux > nb)
			return (0);
		aux++;
	}
	return (0);
}
